  // Dili ayarla
  setLanguage: function(lang) {
    if (this.translations[lang]) {
      this.currentLanguage = lang;
      return true;
    }
    return false;
  },
  
  // Çeviri al
  getMessage: function(key, replacements = {}) {
    const lang = this.currentLanguage;
    if (!this.translations[lang] || !this.translations[lang][key]) {
      // Eğer geçerli dilde çeviri yoksa, İngilizce çeviriyi dene
      if (this.translations["en"] && this.translations["en"][key]) {
        const text = this.translations["en"][key];
        return this.replaceParams(text, replacements);
      }
      return key; // Hiçbir çeviri yoksa, anahtarı döndür
    }
    
    const text = this.translations[lang][key];
    return this.replaceParams(text, replacements);
  },
  
  // Metin içindeki değişkenleri değiştir
  replaceParams: function(text, replacements) {
    let result = text;
    for (const key in replacements) {
      result = result.replace(new RegExp(`{${key}}`, 'g'), replacements[key]);
    }
    return result;
  },
  
  // Sayfadaki tüm çevrilebilir metinleri güncelle
  updatePageTexts: function() {
    const elements = document.querySelectorAll('[data-locale]');
    elements.forEach(element => {
      const key = element.getAttribute('data-locale');
      if (key) {
        // Element türüne göre çeviri uygula
        if (element.tagName === 'INPUT' && element.type === 'text') {
          element.placeholder = this.getMessage(key);
        } else if (element.tagName === 'BUTTON' || element.tagName === 'OPTION') {
          element.textContent = this.getMessage(key);
        } else {
          element.textContent = this.getMessage(key);
        }
      }
    });
  },
  
  // Özel etiketleri güncelle - etiketlerin yanında iki nokta üst üste ekle
  updateLabels: function() {
    try {
      // Tüm etiketleri bul ve güncelle
      const labelContainers = [
        { id: '#username', key: 'username' },
        { id: '#itemCount', key: 'itemCount' },
        { id: '#missingItemCount', key: 'missingItemCount' }
      ];
      
      labelContainers.forEach(container => {
        const labelElement = document.querySelector(`${container.id} span:first-child`);
        if (labelElement) {
          // Etiketi çeviri ile güncelle ve iki nokta üst üste ekle
          labelElement.textContent = this.getMessage(container.key) + ':';
          
          // Değer kısmını al
          const valueElement = document.querySelector(`${container.id} span:last-child`);
          if (valueElement && valueElement.textContent === '') {
            // Eğer değer boşsa, varsayılan '-' değerini ata
            valueElement.textContent = '-';
          }
        }
      });
    } catch (error) {
      console.error('Etiketleri güncelleme hatası:', error);
    }
  }
